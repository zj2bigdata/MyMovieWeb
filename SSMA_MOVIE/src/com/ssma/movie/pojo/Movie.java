package com.ssma.movie.pojo;

public class Movie {
	private String movie_Id;
	private String movie_Name;
	private String movie_Director;
	private String movie_Actor;
	private String movie_Info;
	private String movie_Language;
	private String movie_Kind;
	private String movie_Photo;
	private String movie_Long;
	private String movie_Date;
	private String movie_Edition;
	private String play_Price;

	public String getMovie_Id() {
		return movie_Id;
	}
	public void setMovie_Id(String movie_Id) {
		this.movie_Id = movie_Id;
	}
	public String getMovie_Name() {
		return movie_Name;
	}
	public void setMovie_Name(String movie_Name) {
		this.movie_Name = movie_Name;
	}
	public String getMovie_Director() {
		return movie_Director;
	}
	public void setMovie_Director(String movie_Director) {
		this.movie_Director = movie_Director;
	}
	public String getMovie_Actor() {
		return movie_Actor;
	}
	public void setMovie_Actor(String movie_Actor) {
		this.movie_Actor = movie_Actor;
	}
	public String getMovie_Info() {
		return movie_Info;
	}
	public void setMovie_Info(String movie_Info) {
		this.movie_Info = movie_Info;
	}
	public String getMovie_Language() {
		return movie_Language;
	}
	public void setMovie_Language(String movie_Language) {
		this.movie_Language = movie_Language;
	}
	public String getMovie_Kind() {
		return movie_Kind;
	}
	public void setMovie_Kind(String movie_Kind) {
		this.movie_Kind = movie_Kind;
	}
	public String getMovie_Photo() {
		return movie_Photo;
	}
	public void setMovie_Photo(String movie_Photo) {
		this.movie_Photo = movie_Photo;
	}
	public String getMovie_Long() {
		return movie_Long;
	}
	public void setMovie_Long(String movie_Long) {
		this.movie_Long = movie_Long;
	}
	public String getMovie_Date() {
		return movie_Date;
	}
	public void setMovie_Date(String movie_Date) {
		this.movie_Date = movie_Date;
	}
	public String getMovie_Edition() {
		return movie_Edition;
	}
	public void setMovie_Edition(String movie_Edition) {
		this.movie_Edition = movie_Edition;
	}
	public String getPlay_Price() {
		return play_Price;
	}
	public void setPlay_Price(String play_Price) {
		this.play_Price = play_Price;
	}
	
}
